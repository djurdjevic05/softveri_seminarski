    private void txtFilterKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFilterKeyReleased
        String filter = txtFilter.getText().trim();
        Clan c = new Clan();
        c.setAdresa(filter);
        c.setIme(filter);
        c.setPrezime(filter);
        c.setJmbg(filter);

        List<Clan> lista = Kontroler.getInstance().vratiListuClanova(c);
        
        ModelTabeleClan mtc = (ModelTabeleClan) tblClan.getModel();
        mtc.setListaClanova(lista);

    }//GEN-LAST:event_txtFilterKeyReleased

