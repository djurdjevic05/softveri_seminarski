    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int row = tblClan.getSelectedRow();
        if (row != -1) {
            ModelTabeleClan mtc = (ModelTabeleClan) tblClan.getModel();
            Clan c = mtc.getListaClanova().get(row);
            ServerskiOdgovor so = Kontroler.getInstance().obrisiClana(c);
            if(!so.isUspesno()){
                JOptionPane.showMessageDialog(this, so.getPoruka(),"Greska",JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(this, so.getPoruka(), "Brisanje clana", JOptionPane.INFORMATION_MESSAGE);

        } else {
            JOptionPane.showMessageDialog(this, "Clan nije izabran!","Greska",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzaberiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzaberiActionPerformed
        int row = tblClan.getSelectedRow();
        if (row != -1) {
            ModelTabeleClan mtc = (ModelTabeleClan) tblClan.getModel();
            Clan c = mtc.getListaClanova().get(row);
            if(c == null){
                JOptionPane.showMessageDialog(this, "Clan nije pronadjen!","Greska!",JOptionPane.ERROR_MESSAGE);
                return;
            }
            Kontroler.getInstance().getMapa().put("izabrani_clan", c);
            JOptionPane.showMessageDialog(this, "Prikaz podataka o clanu sa ID-jem: " + c.getClanID());
            new FClan(null, true, FormMode.VIEW).setVisible(true);
            

        } else {
            JOptionPane.showMessageDialog(this, "Clan nije izabran!","Greska",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzaberiActionPerformed

